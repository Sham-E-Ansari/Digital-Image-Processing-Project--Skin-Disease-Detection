function button2_Callback(hObject, eventdata, handles)
% hObject    handle to button2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
name = getGlobalName();
I = imread(name);
I= imresize(I,[256,256]);
I = im2bw(I);
K =  filterImage(I);
axes(handles.axes2);
imshow(K);


K= findErosion(I);
axes(handles.axes3);
imshow(K);
count = 0;
CC = bwconncomp(K);
if(CC.NumObjects >=1)
    [row,col] = size(K);
    for i= 1:row
       for j=1:col
          if(K(i,j)== 0)
              count = count+1;
          end
        end
    end
end
if(count>=2000)
    set(handles.text6,'String','Detected');
else
    set(handles.text6,'String','Not Detected');
end


